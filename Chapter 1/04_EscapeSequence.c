#include <stdio.h>

int main (){

    // \n is used to print new line
    printf("He is Drunk. \nI am not Drunk");

    printf("\n");

    // \t is used to give tab space
    printf("Fruit: \tApple"); 

    printf("\n");

    // \' to give single Quote, \\ for giving slash.
    printf("Path: \"C:\\Temp\"");
    

    return 0;
}