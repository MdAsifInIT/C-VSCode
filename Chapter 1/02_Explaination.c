#include <stdio.h>
// This line is a preprocessor directive that tells the compiler to include the standard input/output library (stdio.h). 
// It provides functions like printf for printing to the console.

int main (){
// This is the main function, and it’s the entry point of any C program. 
// The int before main indicates that the function returns an integer value. 
// The parentheses () denote that this function takes no arguments.
// These curly braces define the scope of the main function. 
// Everything inside these braces belongs to the main function. 


printf("Hello World"); 
// This line uses the printf function to print the string “Hello World” to the console. 
// The \n character is not included, so the output won’t have a newline after it.
    
return 0;
// The return statement indicates the end of the main function. 
// The value 0 is returned to the operating system, indicating successful execution.       

}